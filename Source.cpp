float lastFrame = 0.0f;

glm::vec3 lightPos(1.2f, 1.0f, 2.0f);
// Параметры прожектора (фонарика)
glm::vec3 spotlightPos = camera.Position;   // Позиция == позиция камеры
glm::vec3 spotlightDir = camera.Front;      // Направление == куда смотрит камера
float spotlightCutOff = glm::cos(glm::radians(12.5f));   // Угол внутреннего конуса (12.5°)
float spotlightOuterCutOff = glm::cos(glm::radians(17.5f)); // Угол внешнего конуса (17.5°)
bool spotlightOn = true; // Изначально прожектор включён

